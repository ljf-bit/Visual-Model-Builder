const knownMessageKeys = new Map<string, string>([
  [translations.en['notice.mnistSubset'], 'notice.mnistSubset'],
  [translations.en['notice.fakeDataSubset'], 'notice.fakeDataSubset'],
]);

export function translate(language: AppLanguage, key: string, params: TranslationParams = {}): string {
  const template = translations[language][key] ?? translations.en[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(params[name] ?? `{${name}}`));
}

export function translateKnownMessage(message: string, language: AppLanguage): string {
  const key = knownMessageKeys.get(message);
  return key ? translate(language, key) : message;
}

export function getNodeDescription(type: string, fallback: string | undefined, language: AppLanguage): string | undefined {
  if (language === 'zh') {
    return nodeCopy[type]?.description ?? fallback;
  }
  return fallback;
}

export function getParamLabel(nodeType: string, param: ParamSpec, language: AppLanguage): string {
  if (language === 'zh') {
    return nodeCopy[nodeType]?.params?.[param.key]?.label ?? param.label;
  }
  return param.label;
}

export function getParamHelpText(nodeType: string, param: ParamSpec, language: AppLanguage): string | undefined {
  if (language === 'zh') {
    return nodeCopy[nodeType]?.params?.[param.key]?.helpText ?? param.helpText;
  }
  return param.helpText;
}
